package operadores_relacionales;
public class usa_mayor_igual_que_y_menor_igual_que {
    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 10;
        boolean mayorOIgual = numero1 >= numero2;
        boolean menorOIgual = numero1 <= numero2;
        System.out.println("¿numero 1 es mayor o igual que numero 2?: " + mayorOIgual);
        System.out.println("¿numero 1 es menor o igual que numero 2?: " + menorOIgual);
    }
}