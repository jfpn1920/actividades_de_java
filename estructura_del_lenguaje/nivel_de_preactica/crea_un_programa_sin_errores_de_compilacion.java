package nivel_de_preactica;
public class crea_un_programa_sin_errores_de_compilacion {
    public static void main(String[] args) {
        System.out.println("¡este programa no tiene errores de compilación!");
        int numeroUno = 8;
        int numeroDos = 12;
        int suma = numeroUno + numeroDos;
        System.out.println("la suma de " + numeroUno + " y " + numeroDos + " es: " + suma);
        System.out.println("programa compilado y ejecutado correctamente.");
    }
}