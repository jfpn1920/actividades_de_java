package comentarios_multilinea;
public class mejora_un_programa_agregando_comentarios_claros {
    /*
        Este programa calcula la suma de dos números
        y muestra el resultado en la consola.
        Se agregaron comentarios claros para cada paso.
    */
    public static void main(String[] args) {
        // Declaración de la primera variable y asignación de valor
        int numero1 = 10;
        // Declaración de la segunda variable y asignación de valor
        int numero2 = 5;
        // Suma de las dos variables y almacenamiento en resultado
        int resultado = numero1 + numero2;
        // Muestra el resultado de la suma en la consola
        System.out.println("el resultado de " + numero1 + " + " + numero2 + " es: " + resultado);
        // Mensaje final indicando que el programa ha terminado
        System.out.println("programa finalizado correctamente");
    }
}