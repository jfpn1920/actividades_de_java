package comentarios_multilinea;
public class combina_comentarios_de_una_linea_y_multilinea {
    /* 
        Comentario multilínea:
        Esta clase demuestra cómo combinar ambos tipos de comentarios
        y documentar secciones del programa.
    */
    public static void main(String[] args) {
        // Comentario de una línea explicando la instrucción siguiente
        System.out.println("hola mundo");
        /* Comentario multilínea dentro del main:
            Se puede usar para explicar varias instrucciones a la vez,
            o para bloquear temporalmente varias líneas de código.
        */
        System.out.println("esta es otra linea de codigo");
        // Otra instrucción con comentario de una línea
        System.out.println("fin del ejemplo");
    }
}