package metodo_main;
public class explica_con_comentarios_para_que_sirve_el_metodo_main {
    // El método main es el punto de entrada de cualquier programa en Java.
    // Es el primer método que se ejecuta cuando se corre el programa.
    public static void main(String[] args) {
        // public: permite que el método sea accesible desde cualquier lugar
        // static: permite ejecutar el método sin crear un objeto de la clase
        // void: indica que el método no devuelve ningún valor
        // main: es el nombre especial que Java reconoce como punto de inicio
        // String[] args: permite recibir argumentos desde la línea de comandos
        System.out.println("este es el metodo principal del programa");
    }
}