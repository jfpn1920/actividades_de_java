package comentarios_de_una_sola_linea;
public class explica_con_comentarios_cada_palabra_clave_del_main {
    public static void main(String[] args) {
        // public: permite que el método sea accesible desde cualquier lugar
        // static: permite ejecutar el método sin crear un objeto de la clase
        // void: indica que el método no devuelve ningún valor
        // main: es el nombre del método principal que Java reconoce para iniciar el programa
        // String[] args: permite recibir argumentos desde la línea de comandos
        System.out.println("explicacion del metodo main");
    }
}