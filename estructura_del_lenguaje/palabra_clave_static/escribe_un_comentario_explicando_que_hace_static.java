package palabra_clave_static;
public class escribe_un_comentario_explicando_que_hace_static {
    // La palabra clave static indica que un método o variable pertenece a la clase
    // y no a un objeto. Esto permite usarlo sin necesidad de crear una instancia.
    // En el caso del método main, static permite que Java lo ejecute directamente
    // al iniciar el programa.
    public static void main(String[] args) {
        System.out.println("Ejemplo usando static");
    }
}