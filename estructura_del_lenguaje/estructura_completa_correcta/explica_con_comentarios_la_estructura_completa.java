package estructura_completa_correcta;
/*
    Programa que explica la estructura completa de un programa Java
    con comentarios detallados.
*/
public class explica_con_comentarios_la_estructura_completa {
    /*
        Método main:
        - Es el punto de entrada del programa
        - Todas las instrucciones se ejecutan dentro de este método
    */
    public static void main(String[] args) {
        // Imprime un mensaje de bienvenida
        System.out.println("hola, este programa explica la estructura completa");
        /*
            Declaración de variables:
            - Aquí se pueden definir las variables necesarias
            - Cada variable debe tener un tipo y un nombre
        */
        int numero1 = 10; // Primer número
        int numero2 = 20; // Segundo número
        /*
            Operaciones:
            - Se realizan dentro del main
            - Se pueden usar condicionales, bucles o cálculos
        */
        int suma = numero1 + numero2; // Suma de los dos números
        // Muestra el resultado de la operación
        System.out.println("la suma de " + numero1 + " + " + numero2 + " es: " + suma);
        // Mensaje final indicando que el programa terminó
        System.out.println("programa finalizado correctamente");
    }
}