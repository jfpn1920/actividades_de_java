package estructura_completa_correcta;
/*
    Programa corregido:
    - Código limpio y ordenado
    - Comentarios explicativos
    - Sangría correcta
*/
public class escribe_un_programa_mal_estructurado_y_luego_arreglalo {
    // Método principal: inicio de ejecución
    public static void main(String[] args) {
        // Imprime un mensaje de bienvenida
        System.out.println("hola mundo");
        // Declaración de variables
        int a = 10;
        int b = 5;
        // Suma de variables
        int suma = a + b;
        // Muestra el resultado de la suma
        System.out.println("la suma es: " + suma);
        // Condicional para evaluar la suma
        if (suma > 10) {
            System.out.println("la suma es mayor que 10");
        } else {
            System.out.println("la suma es 10 o menor");
        }
        // Mensaje final
        System.out.println("programa finalizado correctamente");
    }
}