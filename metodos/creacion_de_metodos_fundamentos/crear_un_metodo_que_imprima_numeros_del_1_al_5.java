package creacion_de_metodos_fundamentos;
public class crear_un_metodo_que_imprima_numeros_del_1_al_5 {
    public static void imprimirNumeros() {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
    public static void main(String[] args) {
        imprimirNumeros();
    }
}